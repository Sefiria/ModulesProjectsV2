        public void Reset()
        {
            for (int l = 0; l < z; l++)
            {
                for (int x = 0; x < w; x++)
                {
                    for (int y = 0; y < h; y++)
                    {
                        Tiles[l, x, y] = -1;
                    }
                }
            }
        }
